# Mad Libs Game
noun = input("Enter a noun: ")
verb = input("Enter a verb: ")
adj = input("Enter an adjective: ")
place = input("Enter a place: ")
food = input("Enter a food: ")

print(f"One day, a {adj} {noun} decided to {verb} to {place}.")
print(f"There, it found a giant plate of {food}!")
print(f"It was so {adj} that everyone wanted some.")
print(f"But the {noun} ran away shouting, 'This {food} is mine!'")
print("And that's how the legend of the hungry traveler began.")
